func TestTextReporterOutput(t *testing.T) {
	r := reporter.BuildReport(makeRepoInfo(), makeScore(), makeResults())
	tr := &reporter.TextReporter{}

	var buf bytes.Buffer
	err := tr.Report(context.Background(), r, &buf)
	if err != nil {
		t.Fatalf("Report() error: %v", err)
	}

	output := buf.String()
	if output == "" {
		t.Fatal("Report() produced empty output")
	}

	// Check for ANSI escape codes
	if strings.Contains(output, "\x1b[") || strings.Contains(output, "\033[") {
		t.Error("Output contains ANSI escape codes")
	}

	// Check for required content
	if !strings.Contains(output, "/test/repo") {
		t.Error("Output missing repository path")
	}
	if !strings.Contains(output, "Functional") {
		t.Error("Output missing level")
	}
	if !strings.Contains(output, "75%") {
		t.Error("Output missing pass rate")
	}
}

