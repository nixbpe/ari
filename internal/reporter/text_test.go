func TestTextReporterEmptyResults(t *testing.T) {
	repoInfo := makeRepoInfo()
	score := &scorer.Score{
		Level:    checker.LevelFunctional,
		PassRate: 0.0,
	}
	results := []*checker.Result{}

	r := reporter.BuildReport(repoInfo, score, results)
	tr := &reporter.TextReporter{}

	var buf bytes.Buffer
	err := tr.Report(context.Background(), r, &buf)
	if err != nil {
		t.Fatalf("Report() error: %v", err)
	}

	output := buf.String()
	if output == "" {
		t.Fatal("Report() produced empty output for empty results")
	}

	// Should still have header and footer
	if !strings.Contains(output, "ari — Agent Readiness Index") {
		t.Error("Output missing header for empty results")
	}
	if !strings.Contains(output, "Generated by ari") {
		t.Error("Output missing footer for empty results")
	}
}
